    public DefaultComboBoxModel getModel(){
        DefaultComboBoxModel modelo= new DefaultComboBoxModel();
        for(int i=0;i<pedido.selectPedido().size();i++){
            modelo.addElement(pedido.selectPedido().get(i).getId());
        }
        return modelo;
    }

